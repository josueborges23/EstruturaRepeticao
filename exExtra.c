/*Impondo privacidade com criptografia. O cres-
cimento explosivo das comunicações pela Internet e do
armazenamento de dados nos computadores conectados
à Internet aumentou muito a preocupação com a priva-
cidade. O campo da criptografia trata da codificação de
dados para torná-los difíceis (ou impossíveis, com os es-
quemas mais avançados) de serem lidos por usuários não
autorizados. Nesse exercício, você investigará um esque-
ma simples de codificação e decodificação de dados. Uma
empresa que queira enviar dados pela Internet lhe pediu
que escrevesse um programa que os codificasse para que
pudessem ser transmitidos com mais segurança. Todos
os dados são transmitidos como inteiros de quatro dígi-
tos. Sua aplicação deverá ler um inteiro de quatro dígitos
informado pelo usuário e codificá-lo da seguinte forma:
substitua cada dígito pelo resultado da soma de 7 ao dígi-
to e calcular o módulo depois de dividir o novo valor por
10. Depois, troque o primeiro dígito pelo terceiro e troque
o segundo dígito pelo quarto. A seguir, imprima o inteiro
codificado. Escreva outra aplicação que receba um inteiro
de quatro dígitos codificado e o decodifique (invertendo
o esquema de codificação) para formar o número origi-
nal. [Projeto de leitura opcional: pesquise ‘criptografia
de chave pública’ em geral e o esquema de chave pública
PGP (Pretty Good Privacy) específico. Você também poderá
querer investigar o esquema RSA, que é bastante usado em
aplicações de peso industrial.*/